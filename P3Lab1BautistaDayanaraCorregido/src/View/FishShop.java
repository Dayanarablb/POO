    public void Modify(){
      try {
        int cantidad = 0; // Valor predeterminado para la cantidad
        double costo = 0.0; // Valor predeterminado para el costo       
        // Verificar si los campos de cantidad y costo no estÃ¡n vacios
        String cantidadText = txtQuantity.getText();
        if (!cantidadText.isEmpty()) {
            cantidad = Integer.parseInt(cantidadText);
        } 
        String costoText = txtCost.getText();
        if (!costoText.isEmpty()) {
            costo = Double.parseDouble(costoText);
        }       
        // Llamar al metodo actualizar con los valores obtenidos de los campos
        ma.actualizar(txtCode.getText(), 
                 txtProductos.getText(), 
                 cantidad, // Usar el valor de cantidad obtenido arriba
                 txtFecha.getText(), 
                 cmbStatus.getSelectedItem().toString(),
                 costo, // Usar el valor de costo obtenido arriba
                 cmbConservation.getSelectedItem().toString(),
                 cmbStore.getSelectedItem().toString(),
                 cmbTemperature.getSelectedItem().toString());       
        // Actualizar la tabla y limpiar los campos despuÃ©s de la modificacion
        Tabla1();
        limpiarCampos();
    } catch (NumberFormatException ex) {
        // Manejar la excepcion si ocurre un error al convertir las cadenas a numeros
        JOptionPane.showMessageDialog(null, "Error: AsegÃºrate de ingresar nÃºmeros vÃ¡lidos en los campos de cantidad y costo.");
    }           
    }
    public void Precio(){
    // Obtener la especie seleccionada y la cantidad ingresada por el usuario
    String especie =txtProductos.getText();
    int cantidad = Integer.parseInt(txtQuantity.getText());
    double precio = Double.parseDouble(txtCost.getText());
    // Crear un objeto FisheryProduct con la especie y la cantidad
    fisheryProduct producto = new fisheryProduct();
    producto.setSpecies(especie); // Establecer la especie
    producto.setQuantity(cantidad); // Establecer la cantidad
    producto.setCoste(precio);
    // Calcular el precio total del producto
    double precioTotal = producto.calculateTotalPrice();
    // Construir el nuevo texto con el resultado actual y agregarlo al texto existente
    String nuevoTexto = "Especie: " + especie + "\nPrecio total a pagar: $" + precioTotal + "\n";
    String textoExistente = AreaText.getText();
    String textoActualizado = textoExistente + nuevoTexto;
    // Mostrar el resultado actualizado en el TextArea
    AreaText.setText(textoActualizado);
    }
   
  
    
    
