    private void TablaPescaAlmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaPescaAlmMouseClicked
    
       int fila = TablaPescaAlm.rowAtPoint(evt.getPoint());
    if (fila != -1) { // Verificar si se ha seleccionado una fila vÃ¡lida
        // Obtener los datos de la fila seleccionada
        String codigo = TablaPescaAlm.getValueAt(fila, 0).toString();
        String especie = TablaPescaAlm.getValueAt(fila, 1).toString();
        int cantidad = Integer.parseInt(TablaPescaAlm.getValueAt(fila, 2).toString());
        String fechaPesca = TablaPescaAlm.getValueAt(fila, 3).toString();
        String estado = TablaPescaAlm.getValueAt(fila, 4).toString();
        double precio = Double.parseDouble(TablaPescaAlm.getValueAt(fila, 5).toString());
        String conservacion = TablaPescaAlm.getValueAt(fila, 6).toString();
        String bodegaRefrigerada = TablaPescaAlm.getValueAt(fila, 7).toString();
        String temperatura = TablaPescaAlm.getValueAt(fila, 8).toString();

       

        // Establecer los valores en los campos de texto y otros componentes
        txtCode.setText(codigo);
        txtProductos.setText(especie);
        txtQuantity.setText(String.valueOf(cantidad));
        txtFecha.setText(fechaPesca);
        cmbStatus.setSelectedItem(estado);
        txtCost.setText(String.valueOf(precio));
        cmbConservation.setSelectedItem(conservacion);
        cmbStore.setSelectedItem(bodegaRefrigerada);
        cmbTemperature.setSelectedItem(temperatura);

        // Establecer el documento original
        ma.documentOrigin(codigo, especie, cantidad, fechaPesca, estado, precio, conservacion, bodegaRefrigerada, temperatura);
    }
    }//GEN-LAST:event_TablaPescaAlmMouseClicked

